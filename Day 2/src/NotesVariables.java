
public class NotesVariables {

	public static void main(String[] args) {
		// intro to simple data types in java
		//unlike javascript, every variable must 
		//be strictly declared with a given type
		int numPets; //datatype variableName; 
		
		//code is read top to bottom within functions/methods
		//a variable must be declared before using!
		//asignment statements!
		numPets = 1; 
		
		//print value to console
		System.out.println(numPets);
		
		//2nd datatype of the day!
		//double -> hold decimal numbers! 
		double pi = 3.14; 
		
		System.out.println(pi); 
		
		//we can perform some basic arithmetic in java!
		System.out.println (3*4); //you can multiply
		System.out.println (14/2); //division?
		System.out.println(3-2); //subtraction
		System.out.println(5+6); //add
		//System.out.println (3^6) exponents- no operator for exponent calculations
		System.out.println(7%5); //remainder

	}

}
